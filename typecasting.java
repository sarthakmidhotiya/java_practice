// narrowing casting
public class Main{
    public static void main(String[] args){
        double number = 87.98;
        int result = (int) number;

        System.out.println("double value" + number);
        System.out.println("int value" + result);
// widening casting
        int number1 = 55;
        double result2 = number1;

        System.out.println("double value" + number1);
        System.out.println("int value" + result1);        

    }

}
